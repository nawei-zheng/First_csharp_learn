/*******************************************************************************
**  Function Name:
**      FmcOsLog
**  Function Description:
**      调试日志接口，功能等同于printf
**  Inputs:
**      const FmcLogReasons_e eReason   调试日志等级
**      const FmcOsStrPtr_t pFormat     打印内容
**  Returns:
**      None
*******************************************************************************/
void FmcOsLog(const FmcLogReasons_e eReason, const FmcOsStrPtr_t pFormat, ...)
{
    va_list Parms;
    va_start(Parms, pFormat);
    (void)vprintf(pFormat, Parms);
    printf("\r\n");
    va_end(Parms);
}


/*******************************************************************************
**  Function Name:
**      FmcOsLogHex
**  Function Description:
**      调试日志接口，HEX输出
**  Inputs:
**      const FmcLogReasons_e eReason   调试日志等级
**      UINT8 *pData                    输入数据地址
**      UINT16 u16Len                   输入数据长度
**  Returns:
**      None
*******************************************************************************/
void FmcOsLogHex(const FmcLogReasons_e eReason, UINT8 *pData, UINT16 u16Len)
{
    UINT16 i;
    for (i = 0; i <  u16Len; i++)
    {
        if ((i % 10 == 0) && (i != 0))
        {
            printf("\r\n");
        }
        printf("%02X ", pData[i]);
    }
    printf("\r\n");
}


